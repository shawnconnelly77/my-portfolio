import React, { Component } from "react";
import Home from './HomeComponent';
import Directory from './DirectoryComponent';
import About from "./AboutComponent";
import Contact from "./ContactComponent";
import CampsiteInfo from './CampsiteInfoComponents';
import Constants from 'expo-constants';
import { View, Platform } from 'react-native';
import { createStackNavigator } from 'react-navigation-stack';
import { createDrawerNavigator } from "react-navigation-drawer";
import { createAppContainer } from 'react-navigation';

//STACK NAVIGATOR FOR DIRECTORY PAGE
const DirectoryNavigator = createStackNavigator(
    {
        Directory: { screen: Directory },
        CampsiteInfo: { screen: CampsiteInfo }
    }, 
    {
        initialRouteName: 'Directory',
        defaultNavigationOptions: {
            headerStyle: {
                backgroundColor: '#5637DD'
            },
            headerTintColor: '#fff',
            headerTitleStyle: {
                color: '#fff'
            }
        }
    }
);

//STACK NAVIGATOR FOR HOME PAGE
const HomeNavigator = createStackNavigator(
    {
        Home: { screen: Home }
    }, 
    {
        defaultNavigationOptions: {
            headerStyle: {
                backgroundColor: '#5637DD'
            },
            headerTintColor: '#fff',
            headerTitleStyle: {
                color: '#fff'
            }
        }
    }
);

//STACK NAVIGATOR FOR ABOUT US PAGE
const AboutNavigator = createStackNavigator(
    {
        About: { screen: About }
    }, 
    {
        defaultNavigationOptions: {
            headerStyle: {
                backgroundColor: '#5637DD'
            },
            headerTintColor: '#fff',
            headerTitleStyle: {
                color: '#fff'
            }
        }
    }
);

//STACK NAVIGATOR FOR CONTACT US PAGE
const ContactNavigator = createStackNavigator(
    {
        Contact: { screen: Contact }
    }, 
    {
        defaultNavigationOptions: {
            headerStyle: {
                backgroundColor: '#5637DD'
            },
            headerTintColor: '#fff',
            headerTitleStyle: {
                color: '#fff'
            }
        }
    }
);

//ROUTE THE STACK NAVIGATORS THROUGH THE DRAWER NAVIGATOR
const MainNavigator = createDrawerNavigator(
    {
        Home: { screen: HomeNavigator },
        Directory: { screen: DirectoryNavigator },
        About: { screen: AboutNavigator },
        Contact: { screen: ContactNavigator }
    },
    {
        drawerBackgroundColor: '#CEC8FF'
    }
);

//TOP LEVEL NAVIGATOR
const AppNavigator = createAppContainer(MainNavigator);

class Main extends Component {
    render() {
        return (
            <View
                style={{
                    flex: 1,
                    paddingTop: Platform.OS === 'ios' ? 0 : Constants.statusBarHeight
            }}>
                <AppNavigator />
            </View>
        );
    }
}

export default Main;